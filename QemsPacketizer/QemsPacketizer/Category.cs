        public Category(string name, double ratio)
        {
            this.Name = name;
            this.Ratio = ratio;

            this.MinimumTossups = (int)ratio;
            if (ratio > this.MinimumTossups)
            {
                this.MaximumTossups = this.MinimumTossups + 1;
            }
            else
            {
                this.MaximumTossups = this.MinimumTossups;
